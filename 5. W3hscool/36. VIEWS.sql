/*La declaracion CREATE VIEW en MySQL
En SQL, una vista es una tabla virtual basada en el conjunto  de resultados de una declaracion SQL
Una vista contiene filas y columnas, como una tabla real. Los campos de una vista son campos de una o
mas tablas reales de la base de datos
Puede agregar declaraciones y funciones SQl a una vista y presentar los datos como si vinieran de una sola tabla
Sintaxis:
CREATE VIEW*/