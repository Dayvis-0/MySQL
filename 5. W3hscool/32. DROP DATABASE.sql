/*La declaracion DROP DATABASE se utiliza para eliminar una base de datos SQL existente
Sintaxis:
DROP DATABASE database_name
NOTA: Tener cuidado antes de eliminar una base de datos. Eliminando, una base de datos provocara la 
perdida de informacion completa almacenada en la base de datos*/

-- La siguiente declaracion SQL elimina la base de datos existente "testDB":
DROP DATABASE testDB;
